import os                                        # 导入os模块
src = "C:\\demo\\test\\dir\\mr\\mrsoft.txt"   # 要重命名的文件
dst = "C:\\demo\\test\\dir\\mr\\mr.txt"        # 重命名后的文件
if os.path.exists(src):                          # 判断文件是否存在
    os.rename(src,dst)                            # 重命名文件
    print("文件重命名完毕！")
else:
    print("文件不存在！")



















































































