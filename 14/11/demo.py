import os                                    # 导入os模块
if os.path.exists("message.txt"):          # 判断文件是否存在
    fileinfo = os.stat("message.txt")      # 获取文件的基本信息
    print("文件完整路径：", os.path.abspath("message.txt")) # 获取文件的完整数路径
    # 输出文件的基本信息
    print("文件大小：",fileinfo.st_size," 字节")
    print("最后一次修改时间：",fileinfo.st_mtime)



















































































