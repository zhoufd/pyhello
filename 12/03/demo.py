class Geese:
    '''雁类'''
    neck = "脖子较长"                       # 定义类属性（脖子）
    wing = "振翅频率高"                     # 定义类属性（翅膀）
    leg = "腿位于身份的中心支点，行走自如"  # 定义类属性（腿）
    def __init__(self):                    # 实例方法（相当于构造方法）
        print("我属于雁类！我有以下特征：")
        print(Geese.neck)                   # 输出脖子的特征
        print(Geese.wing)                   # 输出翅膀的特征
        print(Geese.leg)                    # 输出腿的特征




geese = Geese()                                 # 实例化一个雁类的对象













































