number = int(input("请输入商品7天销量："))      # 输入某个7天商品销量
if number  >= 1000:        # 判断是否符合条件，即输入销量是否大于等于1000
    print("本商品7天销量为A！！")   # 大于等于1000，输出销量评价
elif number  >= 500:       # 判断是否符合条件，即输入销量是否大于等于500
     print("本商品7天销量为B！！")   # 大于等于500，输出销量评价
elif number  >=300:       # 判断是否符合条件，即输入销量是否大于等于300
     print("本商品7天销量为C！！")   # 大于等于300，输出销量评价
else:       # 判断是否符合条件，即输入销量如果小于300
     print("本商品7天销量为D！！")   #小于300，输出销量评价




































































