number = int(input("请输入您的6位奖票号码："))      # 输入奖票号码
if number  == 432678 :        # 判断是否符合条件，即输入奖票号码是否等于432678
    print(number,"你中了本期大奖，请速来领奖！！")   # 等于中奖号码，输出中奖信息
if number  != 432678 :       # 判断是否符合条件，即输入奖票号码不等于432678
    print(number,"你未中本期大奖！！")  # 不等于中奖号码，输出未中奖信息



































































