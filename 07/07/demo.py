str1 = '人生苦短，我用Python!'  # 定义字符串
substr1 = str1[1]                # 截取第2个字符
substr2 = str1[5:]               # 从第6个字符截取
substr3 = str1[:5]               # 从左边开始截取5个字符
substr4 = str1[2:5]              # 截取第3个到第5个字符
print('原字符串：',str1)
print(substr1 + '\n' + substr2 + '\n' + substr3 + '\n' + substr4)
















































